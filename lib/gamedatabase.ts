export function getRandomGame(
  ageRange: string,
  scene: string,
  props: string,
  focus?: string
): Game | null {
  let filteredGames = GAMES_DATABASE.filter(game => {
    return game.ageRange === ageRange && 
           game.scene === scene && 
           game.props === props
  })

  if (focus && filteredGames.length > 0) {
    const focusFilteredGames = filteredGames.filter(game => game.focus === focus)
    if (focusFilteredGames.length > 0) {
      filteredGames = focusFilteredGames
    }
  }

  if (filteredGames.length === 0) {
    return null
  }

  const randomIndex = Math.floor(Math.random() * filteredGames.length)
  return filteredGames[randomIndex]
}
